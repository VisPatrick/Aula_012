#   Big Data é um termo que se refere a conjuntos de dados extremamente grandes e complexos que não podem ser facilmente gerenciados ou analisados usando ferramentas tradicionais.
#   O Big Data é caracterizado por três V's: Volume, Velocidade e Variedade.

#   ENGENHEIRO DE DADOS é o profissional responsável por projetar, construir e manter a infraestrutura de dados necessária para armazenar, 
# processar e analisar grandes volumes de dados. Eles trabalham com tecnologias como Hadoop, Spark, NoSQL, bancos de dados relacionais e 
# não relacionais, e são responsáveis por garantir que os dados estejam disponíveis, acessíveis e seguros.

#   CIÊNCIA DE DADOS é o campo que envolve a coleta, análise e interpretação de grandes volumes de dados para extrair insights e
# tomar decisões informadas. Os cientistas de dados usam técnicas estatísticas, aprendizado de máquina e visualização de dados para
# descobrir padrões e tendências nos dados. Eles trabalham com ferramentas como Python, R, SQL, TableaU, MACHINE LEARNING, 
# e são responsáveis por transformar dados brutos em informações valiosas para a tomada de decisões.

#   VISUALIZAÇÃO DE DADOS é a representação gráfica de informações e dados. Ela permite que os usuários compreendam melhor os dados,
# identifiquem padrões, tendências e insights de forma mais intuitiva. As ferramentas de visualização de dados incluem gráficos, tabelas,
# mapas e dashboards interativos. A visualização de dados é uma parte importante do processo de análise de dados, pois ajuda a comunicar
# os resultados de forma clara e eficaz.

#   ANALISTA DE DADOS é o profissional responsável por coletar, processar e analisar dados para extrair insights e tomar decisões informadas.
# Eles trabalham com ferramentas como Excel, SQL, Python e R para manipular dados, criar relatórios e dashboards, e identificar tendências
# e padrões nos dados. Os analistas de dados são essenciais para ajudar as organizações a entenderem melhor seus dados e tomarem decisões baseadas em evidências.

